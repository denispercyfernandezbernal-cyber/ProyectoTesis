    gs.id,
    gs.nombre_completo as grado_seccion,
    COUNT(*) as total_alumnos
FROM alumnos a
JOIN grado_seccion gs ON a.grado_seccion_id = gs.id
WHERE a.nivel = 'SECUNDARIA' AND a.deleted_at IS NULL
GROUP BY gs.id, gs.nombre_completo
ORDER BY gs.id;
